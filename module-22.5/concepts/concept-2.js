// Declare variables using var, let and const keywords and show the differences

var x = 30;
console.log(x);
var x = 35;
console.log(x);
x = 40;
console.log(x);

let y = 50;
console.log(y);
// let y = 55; // This is not allowed
console.log(y);
y = 60;
console.log(y);

const z = 100;
console.log(z);
